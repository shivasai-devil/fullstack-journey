package javabasics.operators;

public class arithmeticOperators {
    public static void main(String[] args){
        long leftOperand=3839283;
        long rightOperand=1202930223;
        System.out.println("Addition: "+(leftOperand+rightOperand));
        System.out.println("Subtraction: "+(leftOperand-rightOperand));
        System.out.println("Multiplication: "+(leftOperand*rightOperand));
        System.out.println("Division: "+(rightOperand/leftOperand));
        System.out.println("Remainder: "+(leftOperand%rightOperand));
    }
}
